// Figura 5.2: ForCounter.java
// Repeticao controlada por contador com a instrucao de repeticao for

public class ForCounter 
{

	public static void main(String[] args) 
	{
		// o cabecalho da instrucao for inclui inicializacao
		// condicao de continuacao do loop e incremento
		for(int counter = 1; counter <= 10; counter ++)
			System.out.printf("%d  ", counter);
		
		System.out.println();
	}
} // fim da classe ForCounter
